a = input('digite algo')
print('o tipo primitivo desse valor é', type(a))
print('só tem espaços?',a.isspace())
#esse de cima é para saber se só foi digitado espaço.
print('é um número?', a.isnumeric())
print('é alfabético?', a.isalpha())
print('é alfanumerico?', a.isalnum())
print('esta em maiuscula?', a.isupper())
print('está em minusculo?', a.islower())
print('está Capitalizado?', a.istitle())
#esse é para saber se esta em maiuscula e minuscula
